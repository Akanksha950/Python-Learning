# 1.Write a Python program to print numbers from 1 to 10 using a while loop.

num=1
while(num<=10):
    print(num)
    num=num+1

print("ques 1 complete")

# 2.Write a program to print numbers from 10 down to 1 using a while loop.

i=10
while(i>=1):
    print(i)
    i=i-1

print("ques 2 complete")



